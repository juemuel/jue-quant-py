# 5.KDJ 随机指标【震荡短周期】
# 5.1 KDJ三线与参数 黄D线慢(稳定线) < 黑/白K线快 < 紫J线更快，如果速度很猛就容易失真，适合震荡的行情
# J线是反映K值和D值的乖离程度,找出阶段性的高点与低点
# 防钝化推荐参数调整：短线K19D3J3，牛市K34D3J3，震荡市K55D3J3，熊市K89D3J3
# 5.2 超买超卖区与背离
# 超买与超卖：涨跌信号易转换，适合操作；
# KDJ超卖区0-20（适合D线，K线-10，J线连续5天趋于0），易涨；建议买入
# KDJ超买区80-100（适合D线，K线+10，J线连续5天趋于100），易跌；建议卖出
# <背离>即价格与指标的反向走势
# 发生顶背离（K线越来越高，但KDJ指标趋于下降），提示原趋势可能结束，建议卖出（可能多头陷阱，洗盘后再涨）
# 发生底背离，提示原趋势可能结束，建议买入（可能技术性反弹，反弹后再跌）
# 空区 < 50 < 多区
